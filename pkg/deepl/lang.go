// LangName returns the Japanese display name for a DeepL language code.
// It accepts codes in any case (e.g. "en", "EN", "En").
func LangName(code string) string {
	if name, ok := langNames[strings.ToUpper(code)]; ok {
		return name
	}
	return code
}
